def quick_sort(arr):
    if len(arr) <= 1:
        return arr
    pivot = arr[-1]
    left = [x for x in arr[:-1] if x < pivot]
    right = [x for x in arr[:-1] if x >= pivot]
    return quick_sort(left) + [pivot] + quick_sort(right)

arr = []
print("Enter 5 numbers:")
for i in range(5):
    arr.append(int(input()))

print("Sorted array:", quick_sort(arr))